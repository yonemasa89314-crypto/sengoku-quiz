export function addFurigana(text) {
  if (!text) return ''
  let result = text
  for (const [kanji, reading] of DICT) {
    if (result.includes(kanji)) {
      result = result.split(kanji).join(
        `<ruby>${kanji}<rt>${reading}</rt></ruby>`
      )
    }
  }
  return result
}
